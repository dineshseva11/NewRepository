package Studyopedia;

public class relational_operator {

	public static void main(String[] args) {
		// relational operator
		int a=10;
		int b=20;
		System.out.println(a==b);  //false
		System.out.println(a<=b);   //true
		System.out.println(a>=b);   //false
		System.out.println(a<b);    //true
		System.out.println(a!=b);    //true
		
	}

}
